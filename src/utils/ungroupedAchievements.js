/**
 * ID достижений из групповых блоков (лист меток + явные списки вкладок).
 * @param {Array<{ rows?: Array }>} sheets
 * @returns {Set<number>}
 */
export function collectGroupedAchievementIds(sheets = []) {
  const ids = new Set([
    ...CHARACTER_UNLOCK_ACHIEVEMENT_IDS,
    ...STORY_PROGRESS_ACHIEVEMENT_IDS,
    ...CHALLENGE_ACHIEVEMENT_IDS,
    ...MISC_ACHIEVEMENT_IDS,
    ...SPECIAL_CONDITION_ACHIEVEMENT_IDS,
    ...DONATION_ACHIEVEMENT_IDS,
  ])
  for (const sheet of sheets) {
    for (const row of sheet.rows || []) {
      for (const cell of row.milestones || []) {
        if (cell.achievementId) ids.add(cell.achievementId)
      }
    }
  }
  return ids
}

/**
 * Номера достижений, которых нет ни в одном групповом блоке.
 * @param {number} achievementCount
 * @param {Iterable<number>} groupedIds
 * @returns {number[]}
 */
export function listUngroupedAchievementIds(achievementCount, groupedIds) {
  const used = groupedIds instanceof Set ? groupedIds : new Set(groupedIds)
  const missing = []
  for (let id = 1; id <= achievementCount; id++) {
    if (!used.has(id)) missing.push(id)
  }
  return missing
}
